import admin from 'firebase-admin';
import { initializeApp } from 'firebase/app';
import { getAuth } from 'firebase/auth';
import dotenv from 'dotenv';

dotenv.config();

// Firebase Admin SDK Configuration (for backend operations)
const adminConfig = {
  projectId: process.env.FIREBASE_PROJECT_ID,
  privateKey: process.env.FIREBASE_PRIVATE_KEY?.replace(/\\n/g, '\n'),
  clientEmail: process.env.FIREBASE_CLIENT_EMAIL,
};

// Initialize Firebase Admin
if (!admin.apps.length) {
  admin.initializeApp({
    credential: admin.credential.cert(adminConfig),
    projectId: process.env.FIREBASE_PROJECT_ID,
  });
}

// Get Firestore instance
export const db = admin.firestore();

// Get Firebase Admin Auth instance
export const adminAuth = admin.auth();

// Firebase Client Configuration (for frontend auth if needed)
const clientConfig = {
  apiKey: process.env.FIREBASE_API_KEY,
  authDomain: process.env.FIREBASE_AUTH_DOMAIN,
  projectId: process.env.FIREBASE_PROJECT_ID,
  storageBucket: process.env.FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.FIREBASE_APP_ID,
};

// Initialize Firebase Client App (optional, for frontend)
export const clientApp = initializeApp(clientConfig);
export const clientAuth = getAuth(clientApp);

// Export admin for other uses
export default admin;
